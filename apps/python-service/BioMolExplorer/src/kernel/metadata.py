PROJECT_NAME = "BioMolExplorer"


PROJECT_DESCRIPTION = (

    "BioMolExplorer is a modular and extensible framework designed "
    "to support structure-based drug discovery through the integration "
    "of molecular data retrieval, cheminformatics, graph-based molecular "
    "analysis, ADMET screening, and virtual screening methodologies. "
    "The framework combines structural bioinformatics and ligand-based "
    "approaches to identify relevant therapeutic targets, investigate "
    "molecular similarity patterns, extract pharmacologically relevant "
    "scaffolds, and generate curated datasets suitable for machine "
    "learning and deep learning applications in computer-aided drug design."

)


PROJECT_INFO = (

    "The framework integrates therapeutic target retrieval from the "
    "Protein Data Bank (PDB), molecular compound mining from ChEMBL "
    "and ZINC databases, automated redocking workflows using AutoDock "
    "Vina, RMSD-based structural evaluation, and graph-oriented molecular "
    "similarity analysis based on Morgan fingerprints and Tanimoto "
    "coefficients. "
    
    "Through graph abstraction and connected component analysis, "
    "BioMolExplorer identifies relevant molecular scaffolds associated "
    "with specific pharmacological groups and therapeutic targets. "
    
    "Additionally, the framework performs automated ADMET and toxicological "
    "screening to remove potentially harmful compounds, producing curated "
    "datasets enriched with biologically and pharmacologically relevant "
    "molecules suitable for virtual screening and artificial intelligence "
    "applications."

)

